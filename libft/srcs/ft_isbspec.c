#include "../../includes/ft_printf.h"

int			ft_isbspec(int c)
{
	char	*str;
	int		i;

	i = 0;
	str = "FfGgEe";
	while (str[i])
	{
		if (str[i] == c)
			return (1);
		i++;
	}
	return (0);
}
