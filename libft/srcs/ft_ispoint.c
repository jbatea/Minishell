#include "../../includes/ft_printf.h"

int		ft_ispoint(char *s, int i)
{
	int	j;

	j = 0;
	while (s[j] && j < i)
	{
		if (s[j] == '.' && s[j + 1])
			return (1);
		j++;
	}
	return (0);
}
